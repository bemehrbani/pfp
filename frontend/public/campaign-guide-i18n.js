let currentGuideLang = "fa";

function setGuideLang(lang) {
    currentGuideLang = lang;
    if (lang === "fa") {
        // Restore original Farsi — just reload
        location.reload();
        return;
    }
    const t = GUIDE_I18N[lang];
    if (!t) return;

    // Direction
    document.documentElement.lang = lang;
    document.documentElement.dir = t.dir;
    document.body.style.direction = t.dir;
    document.title = t.title;

    // Steps LTR fix
    if (t.dir === "ltr") {
        document.querySelectorAll(".steps li").forEach(li => {
            li.style.paddingRight = "0";
            li.style.paddingLeft = "2.5rem";
        });
        document.querySelectorAll(".steps li::before, .callout").forEach(() => { });
        // Inject LTR overrides
        let ltrStyle = document.getElementById("ltr-overrides");
        if (!ltrStyle) {
            ltrStyle = document.createElement("style");
            ltrStyle.id = "ltr-overrides";
            ltrStyle.textContent = `
        .steps li { padding-right: 0 !important; padding-left: 2.5rem !important; }
        .steps li::before { right: auto !important; left: 0 !important; }
        .callout { border-right: none !important; border-left: 3px solid var(--accent) !important;
                   border-radius: 0 var(--radius) var(--radius) 0 !important; }
        .callout.warn { border-left-color: var(--orange) !important; }
        .msg-box { direction: ltr !important; }
        .msg-box .copy-hint { left: auto !important; right: .75rem !important; }
      `;
            document.head.appendChild(ltrStyle);
        }
    }

    // Apply all translations via data-i18n
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = t[el.dataset.i18n];
        if (!key) return;
        if (el.dataset.i18nAttr === "text") {
            el.textContent = key;
        } else {
            el.innerHTML = key;
        }
    });

    // Apply message box texts
    document.querySelectorAll("[data-msg-key]").forEach(el => {
        const key = t[el.dataset.msgKey];
        if (!key) {
            return;
        }
        const hint = el.querySelector(".copy-hint");
        if (hint) hint.textContent = t.copy_hint;
        // Rebuild content: hint + text
        el.innerHTML = `<span class="copy-hint">${t.copy_hint}</span>${key.replace(/\n/g, "\n")}`;
    });

    // Update active button
    document.querySelectorAll(".lang-toggle-btn").forEach(btn => {
        btn.classList.toggle("active", btn.dataset.lang === lang);
    });

    try { localStorage.setItem("guide-lang", lang); } catch (e) { }
}
